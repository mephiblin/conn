      activeNpcDefinitionId,
      DEFAULT_EDITOR_ENCOUNTER_ID: defaultEditorEncounterId,
      DEFAULT_EDITOR_EVENT_ID: defaultEditorEventId,
      DEFAULT_EDITOR_TRAP_EVENT_ID: defaultEditorTrapEventId,
      DEFAULT_EDITOR_SHRINE_EVENT_ID: defaultEditorShrineEventId,
      DEFAULT_EDITOR_REST_EVENT_ID: defaultEditorRestEventId,
