    loadNpcCustomPresets = () => [],
    saveNpcCustomPresets = () => {},
    replaceClassDefinitions = () => {},
    replaceQuestDefinitions = () => {},
    replaceMonsterDefinitions = () => {},
    replaceSkillDefinitions = () => {},
    replaceItemDefinitions = () => {},
    replaceVendorDefinitions = () => {},
    replaceLootTableDefinitions = () => {},
    replaceRarityDefinitions = () => {},
    replaceAffixDefinitions = () => {},
    replaceAffixPoolDefinitions = () => {},
    replaceNpcDefinitions = () => {},
    normalizePlacement = (value) => value,
    computeWalls = () => {},
    refreshPresetCatalog = () => {},
    normalizeTextureId = (id) => id,
    editorEventSelectionDefaults = () => ({}),
    lootTableDefinitionIds = () => [],
    floorTextureIds = [],
    ceilingTextureIds = [],
    wallTextureIds = [],
    defaultFloorTextureId = "",
    defaultCeilingTextureId = "",
    defaultWallTextureId = "",
    eventEditorToPlacementKind = {},
    densityOverlayModes = [],
    addLog = () => {},
    render = () => {},
    localStorageObject = null,
    getCell = () => null,
    roomRectFromPoints = () => ({}),
    cellCoordKey = () => "",
    pointFromCellCoordKey = () => ({}),
    roomCells = () => [],
    roomPlacements = () => [],
    dirs = [],
    vec = {},
    activeNpcDefinitionId = () => null,
    defaultEditorEncounterId = "",
    defaultEditorEventId = "",
    defaultEditorTrapEventId = "",
    defaultEditorShrineEventId = "",
