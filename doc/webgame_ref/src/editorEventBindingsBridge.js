import {
  bindEventBundleCompareControls,
  bindEventBundleFocusedCopyControls,
  bindEventBundlePatchControls,
  bindEventDefinitionEditorControls,
  bindEventDiffDownloadControls,
  bindEventExportArchiveBatchCompareControls,
  bindEventExportArchiveBatchShareControls,
  bindEventExportArchiveDeleteControl,
  bindEventExportArchiveRestoreControl,
  bindEventExportControls,
  bindEventGraphTestControls,
  bindEventRootEffectControls,
  bindEventStepEditorControls,
  bindPlacementOverrideEditorControls,
  bindTrapEventEditorControls,
} from "./renderEditor.js";

export function bindEditorEventInteractions(deps = {}) {
  const {
    eventDef = null,
    eventTool = "",
    eventDefId = "",
    eventPlacementKind = "",
    state,
    render = () => {},
    documentObject = document,
    addLog = () => {},
    parseJsonField = (value) => value,
    updateEventDefinition = () => {},
    activeEventDefinitionId = () => "",
    stopEditorEventTestSession = () => {},
    renameEventPreset = () => false,
    createEventPresetFromDefinition = () => "",
    defaultInteractionTypeForPlacementKind = () => "interact",
    createEventGraphTemplate = () => null,
    uniqueEventStepId = () => "step_start",
    eventEffectFlagValueType = () => "string",
    selectedEventBundleCompareEventId = "",
    eventBundleFocusOptions = [],
    selectedEventBundleFocusPath = "",
    focusedEventBundlePreviousValue = null,
    focusedEventBundleCurrentValue = null,
    focusedEventBundleResolvedValue = null,
    copyTextToClipboard = () => {},
    recordEventBundlePatchHistory = () => {},
    recordEventBundlePatchArchive = () => {},
    selectedEventBundleCompareRow = null,
    selectedEventBundleComparePatch = null,
    selectedEventBundleComparePatchPreview = null,
    eventBundlePatchDraftValue = "",
    defaultEventBundlePatchJson = "",
    selectedEventBundlePatchArchiveEntry = null,
    selectedEventBundleResolvedPreview = null,
    downloadTextFile = () => {},
    applyResolvedEventBundleRow = () => {},
    eventGraphCompactExport = null,
    eventProjectReviewBundle = null,
    currentBundleSummary = null,
    recordEventExportHistory = () => {},
    recordEventExportArchive = () => {},
    selectedEventExportArchiveEntry = null,
    selectedEventExportArchiveBundleRowsForRestore = [],
    effectiveEventExportArchiveFieldKeys = [],
    effectiveEventExportArchiveStepIds = [],
    effectiveEventExportArchiveStepPartKeys = [],
    effectiveEventExportArchiveStepItemKeys = [],
    hasRestorableCompactEventPayload = () => false,
    applyPartialCompactEventRowToDefinition = () => {},
    selectedEventExportArchiveTargetEventId = "",
    deleteEventExportArchiveEntry = () => {},
    eventExportArchiveQuery = "",
    eventExportArchiveBatchCompare = null,
    eventExportArchiveBatchCompareExport = null,
    applyEventExportArchiveBatchCompareTargets = () => {},
    eventExportArchiveBatchShareExport = null,
    eventExportArchiveBatchShareLink = "",
    loadEventExportArchive = () => [],
    isRestorableEventExportEntry = () => false,
    eventExportEntryMatchesQuery = () => false,
    buildEventExportArchiveBatchCompare = () => null,
    importEventExportArchiveBatchShareLink = () => {},
    eventGraphJsonDiffText = "",
    eventBundleJsonDiffText = "",
    selectedEventStepDef = null,
    startEditorEventTestSession = () => {},
    resolveEventChoice = () => {},
    createEventStepTemplate = () => ({}),
    createEventBranchTemplate = () => ({}),
    createEventChoiceTemplate = () => ({}),
    createEventEffectTemplate = () => ({}),
    selectedPlacement = null,
    selectedPlacementEvent = null,
    eventDefinitions = {},
    updatePlacementOverrides = () => {},
    resolvePlacementEvent = () => null,
    toggleRequiredPlacementContract = () => {},
    isRequiredEventPlacement = () => false,
  } = deps;

  if (!documentObject.getElementById("eventInspectorToolSelect")) return;

  if (eventDef) {
    bindEventDefinitionEditorControls({
      state,
      eventTool,
      eventDef,
      eventDefId,
      eventPlacementKind,
      render,
      documentObject,
      addLog,
      parseJsonField,
      updateEventDefinition,
      activeEventDefinitionId,
      stopEditorEventTestSession,
      renameEventPreset,
      createEventPresetFromDefinition,
      defaultInteractionTypeForPlacementKind,
      createEventGraphTemplate,
      uniqueEventStepId,
    });
    bindEventRootEffectControls({
      eventDefId,
      render,
      documentObject,
      updateEventDefinition,
      createEventEffectTemplate,
      eventEffectFlagValueType,
    });
    bindEventBundleCompareControls({
      state,
      eventBundleFocusOptions,
      render,
      documentObject,
    });
    bindEventBundleFocusedCopyControls({
      selectedEventBundleFocusPath,
      selectedEventBundleCompareEventId,
      focusedEventBundlePreviousValue,
      focusedEventBundleCurrentValue,
      focusedEventBundleResolvedValue,
      render,
      documentObject,
      copyTextToClipboard,
      recordEventBundlePatchHistory,
      recordEventBundlePatchArchive,
      addLog,
    });
    bindEventExportControls({
      eventDefId,
      eventGraphCompactExport,
      eventProjectReviewBundle,
      currentBundleSummary,
      render,
      documentObject,
      copyTextToClipboard,
      downloadTextFile,
      recordEventExportHistory,
      recordEventExportArchive,
      addLog,
    });
    bindEventExportArchiveRestoreControl({
      state,
      eventTool,
      selectedEventExportArchiveEntry,
      selectedEventExportArchiveBundleRowsForRestore,
      effectiveEventExportArchiveFieldKeys,
      effectiveEventExportArchiveStepIds,
      effectiveEventExportArchiveStepPartKeys,
      effectiveEventExportArchiveStepItemKeys,
      render,
      documentObject,
      hasRestorableCompactEventPayload,
      applyPartialCompactEventRowToDefinition,
      recordEventExportHistory,
      recordEventExportArchive,
      addLog,
    });
    bindEventExportArchiveDeleteControl({
      state,
      selectedEventExportArchiveEntry,
      selectedEventExportArchiveTargetEventId,
      render,
      documentObject,
      deleteEventExportArchiveEntry,
      recordEventExportHistory,
      addLog,
    });
    bindEventExportArchiveBatchCompareControls({
      eventTool,
      eventExportArchiveQuery,
      eventExportArchiveBatchCompare,
      eventExportArchiveBatchCompareExport,
      render,
      documentObject,
      copyTextToClipboard,
      downloadTextFile,
      applyEventExportArchiveBatchCompareTargets,
      recordEventExportHistory,
      recordEventExportArchive,
      addLog,
    });
    bindEventExportArchiveBatchShareControls({
      state,
      eventTool,
      selectedEventExportArchiveEntry,
      eventExportArchiveBatchShareExport,
      eventExportArchiveBatchShareLink,
      render,
      documentObject,
      copyTextToClipboard,
      loadEventExportArchive,
      isRestorableEventExportEntry,
      eventExportEntryMatchesQuery,
      buildEventExportArchiveBatchCompare,
      applyEventExportArchiveBatchCompareTargets,
      importEventExportArchiveBatchShareLink,
      recordEventExportHistory,
      recordEventExportArchive,
      addLog,
    });
    bindEventDiffDownloadControls({
      eventDefId,
      eventGraphJsonDiffText,
      eventBundleJsonDiffText,
      render,
      documentObject,
      downloadTextFile,
      addLog,
    });
    bindEventBundlePatchControls({
      state,
      selectedEventBundleCompareEventId,
      selectedEventBundleCompareRow,
      selectedEventBundleComparePatch,
      selectedEventBundleComparePatchPreview,
      eventBundlePatchDraftValue,
      defaultEventBundlePatchJson,
      selectedEventBundlePatchArchiveEntry,
      selectedEventBundleResolvedPreview,
      render,
      documentObject,
      downloadTextFile,
      recordEventBundlePatchHistory,
      recordEventBundlePatchArchive,
      applyResolvedEventBundleRow,
      addLog,
    });
    bindEventGraphTestControls({
      eventDefId,
      eventDef,
      selectedEventStepDef,
      render,
      documentObject,
      startEditorEventTestSession,
      stopEditorEventTestSession,
      resolveEventChoice,
    });
    bindEventStepEditorControls({
      state,
      eventDefId,
      selectedEventStepDef,
      render,
      documentObject,
      updateEventDefinition,
      stopEditorEventTestSession,
      createEventStepTemplate,
      createEventBranchTemplate,
      createEventChoiceTemplate,
      createEventEffectTemplate,
      eventEffectFlagValueType,
    });
    if (eventTool === "trap") {
      bindTrapEventEditorControls({
        eventDefId,
        documentObject,
        updateEventDefinition,
      });
    }
  }

  if (selectedPlacement && selectedPlacementEvent) {
    bindPlacementOverrideEditorControls({
      state,
      selectedPlacement,
      selectedPlacementEvent,
      eventDefinitions,
      eventTool,
      render,
      documentObject,
      addLog,
      updatePlacementOverrides,
      resolvePlacementEvent,
      createEventPresetFromDefinition,
      defaultInteractionTypeForPlacementKind,
      toggleRequiredPlacementContract,
      isRequiredEventPlacement,
    });
  }
}
