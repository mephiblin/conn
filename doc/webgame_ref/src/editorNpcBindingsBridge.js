import {
  bindNpcCustomPresetApplyControls,
  bindNpcCustomPresetArchiveControls,
  bindNpcCustomPresetSelectionControls,
  bindNpcDefinitionEditorControls,
  bindNpcPlacementEditorControls,
  bindNpcQuestSeedEditorControls,
  bindNpcSelectedServiceEditorControls,
  bindNpcServiceEditorControls,
} from "./renderEditor.js";

export function bindEditorNpcInteractions(deps = {}) {
  const {
    npcDef = null,
    npcDefId = "",
    state,
    render = () => {},
    documentObject = document,
    addLog = () => {},
    parseJsonField = (value) => value,
    updateNpcDefinition = () => {},
    createNpcQuestHookTemplate = () => ({}),
    createQuestSeedTemplate = () => ({}),
    duplicateQuestSeedTemplate = () => ({}),
    loadNpcCustomPresets = () => [],
    saveNpcCustomPresets = () => {},
    buildNpcCustomPresetFromDefinition = () => ({}),
    defaultNpcPresetSelectionIndexes = () => [],
    defaultNpcPresetDialogueSelectionMap = () => new Map(),
    defaultNpcPresetDialogueChoiceSelectionMap = () => new Map(),
    defaultNpcPresetDialogueBranchSelectionMap = () => new Map(),
    defaultNpcPresetServiceFieldSelectionMap = () => new Map(),
    defaultNpcPresetSeedFieldSelectionMap = () => new Map(),
    buildNpcCustomPresetDiff = () => null,
    selectedNpcCustomPreset = null,
    npcCustomPresetDiff = null,
    selectedNpcPresetServiceIndexes = [],
    selectedNpcPresetSeedIndexes = [],
    selectedNpcPresetDialogueStepIndexes = [],
    selectedNpcPresetDialogueChoiceIndexes = [],
    selectedNpcPresetDialogueBranchIndexes = [],
    selectedNpcPresetServiceFieldNames = [],
    selectedNpcPresetSeedFieldNames = [],
    npcPresetServiceIdentity = () => "",
    npcPresetSeedIdentity = () => "",
    npcDialogueStepIdentity = () => "",
    npcDialogueChoiceIdentity = () => "",
    npcDialogueBranchIdentity = () => "",
    npcCustomPresetMergePatch = null,
    npcCustomPresetMergePatchDraftValue = "",
    npcCustomPresetMergePatchPreview = null,
    selectedNpcPresetPatchArchiveEntry = null,
    filteredNpcPresetPatchArchive = [],
    npcPresetPatchArchiveQuery = "",
    latestNpcPresetUndoEntry = null,
    latestNpcPresetRedoEntry = null,
    npcPresetPatchHistory = [],
    npcPresetRedoEntries = [],
    selectedNpcPresetRedoArchiveEntry = null,
    downloadTextFile = () => {},
    copyTextToClipboard = () => {},
    readTextFromClipboard = () => Promise.resolve(""),
    captureNpcPresetUndoSnapshot = () => {},
    clearNpcPresetRedoEntry = () => {},
    recordNpcPresetPatchHistory = () => {},
    recordNpcPresetPatchArchive = () => {},
    applyNpcPresetMergePatchToDefinition = () => null,
    deleteNpcPresetPatchArchiveEntry = () => {},
    deleteNpcPresetPatchArchiveEntries = () => {},
    pushNpcPresetRedoEntry = () => {},
    applyNpcPresetUndoSnapshot = () => {},
    deleteNpcPresetRedoArchiveEntry = () => {},
    selectedNpcServiceDef = null,
    createNpcServiceTemplate = () => ({}),
    createNpcServiceGroupTemplates = () => [],
    items = {},
    selectedNpcQuestSeedDef = null,
    questRewardFlagValueType = () => "string",
    createNpcDialogueStepTemplate = () => ({}),
    createNpcDialogueChoiceTemplate = () => ({}),
    selectedNpcPlacement = null,
    toggleRequiredPlacementContract = () => {},
    isRequiredNpcPlacement = () => false,
  } = deps;

  if (documentObject.getElementById("workbenchProfileSelect")) return;

  if (npcDef) {
    bindNpcDefinitionEditorControls({
      state,
      npcDefId,
      npcDef,
      render,
      documentObject,
      addLog,
      parseJsonField,
      updateNpcDefinition,
      createNpcQuestHookTemplate,
      createQuestSeedTemplate,
      duplicateQuestSeedTemplate,
      loadNpcCustomPresets,
      saveNpcCustomPresets,
      buildNpcCustomPresetFromDefinition,
      defaultNpcPresetSelectionIndexes,
      defaultNpcPresetDialogueSelectionMap,
      defaultNpcPresetDialogueChoiceSelectionMap,
      defaultNpcPresetDialogueBranchSelectionMap,
      defaultNpcPresetServiceFieldSelectionMap,
      defaultNpcPresetSeedFieldSelectionMap,
      buildNpcCustomPresetDiff,
    });
    bindNpcCustomPresetSelectionControls({
      state,
      selectedNpcCustomPreset,
      npcCustomPresetDiff,
      render,
      documentObject,
      selectedNpcPresetServiceIndexes,
      selectedNpcPresetSeedIndexes,
      selectedNpcPresetDialogueStepIndexes,
      selectedNpcPresetDialogueChoiceIndexes,
      selectedNpcPresetDialogueBranchIndexes,
      selectedNpcPresetServiceFieldNames,
      selectedNpcPresetSeedFieldNames,
    });
    bindNpcCustomPresetApplyControls({
      state,
      npcDefId,
      npcDef,
      npcCustomPresetDiff,
      render,
      documentObject,
      addLog,
      loadNpcCustomPresets,
      saveNpcCustomPresets,
      updateNpcDefinition,
      npcPresetServiceIdentity,
      npcPresetSeedIdentity,
      selectedNpcPresetServiceIndexes,
      selectedNpcPresetSeedIndexes,
      selectedNpcPresetServiceFieldNames,
      selectedNpcPresetSeedFieldNames,
      selectedNpcPresetDialogueStepIndexes,
      selectedNpcPresetDialogueChoiceIndexes,
      selectedNpcPresetDialogueBranchIndexes,
      npcDialogueStepIdentity,
      npcDialogueChoiceIdentity,
      npcDialogueBranchIdentity,
      defaultNpcPresetSelectionIndexes,
      defaultNpcPresetDialogueSelectionMap,
      defaultNpcPresetDialogueChoiceSelectionMap,
      defaultNpcPresetDialogueBranchSelectionMap,
      defaultNpcPresetServiceFieldSelectionMap,
      defaultNpcPresetSeedFieldSelectionMap,
      buildNpcCustomPresetDiff,
    });
    bindNpcCustomPresetArchiveControls({
      state,
      npcDefId,
      selectedNpcCustomPreset,
      npcCustomPresetMergePatch,
      npcCustomPresetMergePatchDraftValue,
      npcCustomPresetMergePatchPreview,
      selectedNpcPresetPatchArchiveEntry,
      filteredNpcPresetPatchArchive,
      npcPresetPatchArchiveQuery,
      latestNpcPresetUndoEntry,
      latestNpcPresetRedoEntry,
      npcPresetPatchHistory,
      npcPresetRedoEntries,
      selectedNpcPresetRedoArchiveEntry,
      render,
      documentObject,
      addLog,
      downloadTextFile,
      copyTextToClipboard,
      readTextFromClipboard,
      captureNpcPresetUndoSnapshot,
      clearNpcPresetRedoEntry,
      recordNpcPresetPatchHistory,
      recordNpcPresetPatchArchive,
      applyNpcPresetMergePatchToDefinition,
      updateNpcDefinition,
      deleteNpcPresetPatchArchiveEntry,
      deleteNpcPresetPatchArchiveEntries,
      pushNpcPresetRedoEntry,
      applyNpcPresetUndoSnapshot,
      deleteNpcPresetRedoArchiveEntry,
    });
    bindNpcServiceEditorControls({
      state,
      npcDefId,
      selectedNpcServiceDef,
      render,
      documentObject,
      updateNpcDefinition,
      createNpcServiceTemplate,
      createNpcServiceGroupTemplates,
    });
    bindNpcQuestSeedEditorControls({
      state,
      items,
      selectedNpcQuestSeedDef,
      render,
      documentObject,
      updateNpcDefinition,
      npcDefId,
      questRewardFlagValueType,
    });
    bindNpcSelectedServiceEditorControls({
      state,
      npcDefId,
      selectedNpcServiceDef,
      render,
      documentObject,
      addLog,
      parseJsonField,
      updateNpcDefinition,
      createNpcDialogueStepTemplate,
      createNpcDialogueChoiceTemplate,
    });
  }

  bindNpcPlacementEditorControls({
    selectedNpcPlacement,
    state,
    render,
    documentObject,
    toggleRequiredPlacementContract,
    isRequiredNpcPlacement,
    addLog,
  });
}
