import {
  bindClassDefinitionEditorControls,
  bindEditorWorkspaceActionControls,
  bindItemAuthoringSupportControls,
  bindItemDefinitionEditorControls,
  bindLootTableAdvancedControls,
  bindLootTableEditorControls,
  bindMonsterDefinitionEditorControls,
  bindQuestDefinitionEditorControls,
  bindSkillDefinitionEditorControls,
  bindPresetDraftControls,
  bindPresetGenerationControls,
  bindVendorDefinitionEditorControls,
  bindVendorRotationEditorControls,
} from "./renderEditor.js";

export function bindEditorContentInteractions(deps = {}) {
  const {
    state,
    render,
    documentObject,
    addLog,
    classDef,
    classDefIndex,
    parseJsonField,
    updateClassDefinition,
    questDef,
    questDefId,
    questDefinitions,
    replaceQuestDefinitions,
    activeQuestDefinitionId,
    uniqueQuestDefinitionId,
    createQuestDefinitionTemplate,
    updateQuestDefinition,
    buildGeneratedQuestDefinition,
    monsterDef,
    monsterDefId,
    monsters,
    replaceMonsterDefinitions,
    activeMonsterDefinitionId,
    uniqueMonsterDefinitionId,
    createMonsterDefinitionTemplate,
    updateMonsterDefinition,
    skillDef,
    skillDefId,
    skills,
    replaceSkillDefinitions,
    activeSkillDefinitionId,
    uniqueSkillDefinitionId,
    createSkillDefinitionTemplate,
    updateSkillDefinition,
    itemDef,
    itemDefId,
    items,
    replaceItemDefinitions,
    activeItemDefinitionId,
    uniqueItemDefinitionId,
    createItemDefinitionTemplate,
    updateItemDefinition,
    vendorDef,
    vendorDefId,
    vendors,
    replaceVendorDefinitions,
    activeVendorDefinitionId,
    uniqueVendorDefinitionId,
    createVendorDefinitionTemplate,
    updateVendorDefinition,
    ensureVendorInventoryEntryObject,
    setGeneratedEntryFields,
    selectedVendorRotation,
    selectedVendorRotationState,
    createVendorRotationTemplate,
    lootTableDef,
    lootTableId,
    lootTables,
    replaceLootTableDefinitions,
    lootTableDefinitionIds,
    activeLootTableId,
    uniqueLootTableId,
    createLootTableDefinitionTemplate,
    updateLootTableDefinition,
    ensureLootEntryObject,
    createLootEntryTemplate,
    selectedLootTier,
    selectedLootTierState,
    selectedLootBonus,
    selectedLootBonusState,
    selectedCombatRewardProfile,
    selectedCombatRewardProfileState,
    createLootTierTemplate,
    createLootBonusTemplate,
    createCombatRewardProfileTemplate,
    rarityDef,
    rarityDefId,
    affixDef,
    affixDefId,
    affixPoolDef,
    affixPoolId,
    rarityDefinitions,
    affixDefinitions,
    affixPoolDefinitions,
    uniqueSchemaId,
    createRarityDefinitionTemplate,
    updateRarityDefinition,
    replaceRarityDefinitions,
    createAffixDefinitionTemplate,
    updateAffixDefinition,
    replaceAffixDefinitions,
    createAffixPoolDefinitionTemplate,
    updateAffixPoolDefinition,
    replaceAffixPoolDefinitions,
    buildSampleItemPreview,
    makeGeneratedItemInstance,
    pushInventoryEntry,
    inventoryEntryLabel,
    preset,
    randomMapSeed,
    makeMap,
    applyDraftFromPreset,
    firstValidationIssue,
    validationIssueRepairHint,
    validationSummaryText,
    saveEditorProject,
    loadEditorProject,
    cloneEditorMap,
    buildEditorProject,
    buildCompiledMapForRuntime,
    buildContentBuildManifest,
    applyEditorProject,
    normalizeMapMetadata,
    computeWalls,
    startTestPlaySession,
    presetDraftSize,
    createEmptyPresetGrid,
    createDraftPreset,
    draftFromGrid,
    upsertCustomPreset,
    refreshPresetCatalog,
    deleteCustomPreset,
  } = deps;

  if (documentObject.getElementById("workbenchProfileSelect")) return;

  bindClassDefinitionEditorControls({
    state,
    classDef,
    classDefIndex,
    render,
    documentObject,
    addLog,
    parseJsonField,
    updateClassDefinition,
  });

  bindItemDefinitionEditorControls({
    state,
    itemDef,
    itemDefId,
    render,
    documentObject,
    addLog,
    items,
    parseJsonField,
    replaceItemDefinitions,
    activeItemDefinitionId,
    uniqueItemDefinitionId,
    createItemDefinitionTemplate,
    updateItemDefinition,
  });

  bindQuestDefinitionEditorControls({
    state,
    questDef,
    questDefId,
    render,
    documentObject,
    addLog,
    questDefinitions,
    parseJsonField,
    replaceQuestDefinitions,
    activeQuestDefinitionId,
    uniqueQuestDefinitionId,
    createQuestDefinitionTemplate,
    updateQuestDefinition,
    buildGeneratedQuestDefinition,
  });

  bindMonsterDefinitionEditorControls({
    state,
    monsterDef,
    monsterDefId,
    render,
    documentObject,
    addLog,
    monsters,
    parseJsonField,
    replaceMonsterDefinitions,
    activeMonsterDefinitionId,
    uniqueMonsterDefinitionId,
    createMonsterDefinitionTemplate,
    updateMonsterDefinition,
  });

  bindSkillDefinitionEditorControls({
    state,
    skillDef,
    skillDefId,
    render,
    documentObject,
    addLog,
    skills,
    parseJsonField,
    replaceSkillDefinitions,
    activeSkillDefinitionId,
    uniqueSkillDefinitionId,
    createSkillDefinitionTemplate,
    updateSkillDefinition,
  });

  bindVendorDefinitionEditorControls({
    state,
    vendorDef,
    vendorDefId,
    render,
    documentObject,
    addLog,
    items,
    vendors,
    parseJsonField,
    replaceVendorDefinitions,
    activeVendorDefinitionId,
    uniqueVendorDefinitionId,
    createVendorDefinitionTemplate,
    updateVendorDefinition,
    ensureVendorInventoryEntryObject,
    setGeneratedEntryFields,
  });

  if (vendorDef) {
    bindVendorRotationEditorControls({
      state,
      vendorDefId,
      selectedVendorRotation,
      selectedVendorRotationState,
      render,
      documentObject,
      items,
      updateVendorDefinition,
      createVendorRotationTemplate,
      ensureVendorInventoryEntryObject,
      setGeneratedEntryFields,
    });
  }

  bindLootTableEditorControls({
    state,
    lootTableDef,
    lootTableId,
    render,
    documentObject,
    addLog,
    lootTables,
    parseJsonField,
    replaceLootTableDefinitions,
    lootTableDefinitionIds,
    activeLootTableId,
    uniqueLootTableId,
    createLootTableDefinitionTemplate,
    updateLootTableDefinition,
    ensureLootEntryObject,
    setGeneratedEntryFields,
    createLootEntryTemplate,
  });

  if (lootTableDef) {
    bindLootTableAdvancedControls({
      state,
      lootTableId,
      selectedLootTier,
      selectedLootTierState,
      selectedLootBonus,
      selectedLootBonusState,
      selectedCombatRewardProfile,
      selectedCombatRewardProfileState,
      render,
      documentObject,
      items,
      lootTables,
      updateLootTableDefinition,
      ensureLootEntryObject,
      setGeneratedEntryFields,
      createLootEntryTemplate,
      createLootTierTemplate,
      createLootBonusTemplate,
      createCombatRewardProfileTemplate,
    });
  }

  bindItemAuthoringSupportControls({
    state,
    rarityDef,
    rarityDefId,
    affixDef,
    affixDefId,
    affixPoolDef,
    affixPoolId,
    itemDefId,
    render,
    documentObject,
    addLog,
    rarityDefinitions,
    affixDefinitions,
    affixPoolDefinitions,
    parseJsonField,
    uniqueSchemaId,
    createRarityDefinitionTemplate,
    updateRarityDefinition,
    replaceRarityDefinitions,
    createAffixDefinitionTemplate,
    updateAffixDefinition,
    replaceAffixDefinitions,
    createAffixPoolDefinitionTemplate,
    updateAffixPoolDefinition,
    replaceAffixPoolDefinitions,
    buildSampleItemPreview,
    makeGeneratedItemInstance,
    pushInventoryEntry,
    inventoryEntryLabel,
  });

  bindPresetGenerationControls({
    state,
    preset,
    render,
    documentObject,
    addLog,
    randomMapSeed,
    makeMap,
    applyDraftFromPreset,
  });

  bindEditorWorkspaceActionControls({
    state,
    render,
    documentObject,
    addLog,
    startTestPlaySession,
    firstValidationIssue,
    validationIssueRepairHint,
    validationSummaryText,
    saveEditorProject,
    loadEditorProject,
    cloneEditorMap,
    buildEditorProject,
    buildCompiledMapForRuntime,
    buildContentBuildManifest,
    applyEditorProject,
    normalizeMapMetadata,
    computeWalls,
  });

  bindPresetDraftControls({
    state,
    presetDraftSize,
    render,
    documentObject,
    addLog,
    createEmptyPresetGrid,
    createDraftPreset,
    draftFromGrid,
    upsertCustomPreset,
    refreshPresetCatalog,
    deleteCustomPreset,
  });
}
