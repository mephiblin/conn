        documentObject,
        createViewController,
        viewController,
        setViewController,
        dragLookEnabled,
        inventoryOverlayOpen,
        materialManifest,
        rendererLodProfile,
        selectedPreset,
        resolveInteractionCandidate,
        dirs,
        vec,
