export function attachEditorStateAccessors(target, ensureEditorState = null) {
  for (const field of EDITOR_STATE_FIELDS) {
    Object.defineProperty(target, field, {
      configurable: true,
      enumerable: false,
      get() {
        if (!this.editor && typeof ensureEditorState === "function") ensureEditorState();
        return this.editor[field];
      },
      set(value) {
        if (!this.editor && typeof ensureEditorState === "function") ensureEditorState();
        this.editor[field] = value;
      },
    });
  }
}

