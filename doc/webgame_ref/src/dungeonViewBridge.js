import {
  bindInteractionOverlay,
  drawDungeonView,
  inventoryEntryOverlayMarkup,
  inventoryOverlayMarkup,
  interactionOverlayMarkup,
} from "./renderGame.js";

export function renderDungeonViewBridge(deps = {}) {
  const {
    state,
    documentObject = document,
    createViewController = () => null,
    viewController = null,
    setViewController = () => {},
    dragLookEnabled = false,
    inventoryOverlayOpen = () => false,
    materialManifest = {},
    rendererLodProfile = "default",
    selectedPreset = () => null,
    resolveInteractionCandidate = () => ({ dir: "north", placements: [], cell: null, target: { x: 0, y: 0 } }),
    dirs = [],
    vec = {},
    interactivePlacementKinds = new Set(),
    currentMapSeed = () => null,
    escapeHtml = (value) => String(value ?? ""),
    items = {},
    sortedFilteredInventoryEntries = () => [],
    inventoryManualReorderEnabled = () => false,
    normalizeInventoryList = (value) => value || [],
    inventoryEntryEquipmentSlot = () => "",
    inventoryEntryItemId = () => "",
    inventoryEntryDetailText = () => "",
    compareCandidateSummary = () => "",
    inventoryFilterOptions = () => [],
    inventorySortOptions = () => [],
    inventorySummaryText = () => "",
    heroEquipmentEntries = () => ({}),
    inventoryEntryLabel = () => "",
    inventoryEntryKindLabel = () => "",
    resolveEventChoice = () => {},
    resolveNpcService = () => {},
    closeInteraction = () => {},
    render = () => {},
    hostId = "view",
    overlayId = "viewOverlay",
    activeModes = ["dungeon"],
    inventoryMode = "dungeon",
  } = deps;

  const nextViewController = drawDungeonView({
    state,
    documentObject,
    createViewController,
    viewController,
    setViewController,
    dragLookEnabled,
    inventoryOverlayOpen,
    materialManifest,
    rendererLodProfile,
    selectedPreset,
    resolveInteractionCandidate,
    dirs,
    vec,
    interactivePlacementKinds,
    currentMapSeed,
    interactionOverlayMarkup: () => interactionOverlayMarkup({
      state,
      mode: state.mode,
      interactiveModes: activeModes,
      escapeHtml,
    }),
    inventoryOverlayMarkup: () => inventoryOverlayMarkup({
      state,
      items,
      inventoryOverlayOpen: () => inventoryOverlayOpen(inventoryMode),
      sortedFilteredInventoryEntries,
      inventoryManualReorderEnabled,
      normalizeInventoryList,
      inventoryEntryEquipmentSlot,
      compareCandidateSummary,
      escapeHtml,
      inventoryFilterOptions,
      inventorySortOptions,
      inventorySummaryText,
      heroEquipmentEntries,
      inventoryEntryLabel,
      inventoryEntryDetailText,
      inventoryEntryKindLabel,
      inventoryEntryOverlayMarkupImpl: ({ entry, index }) => inventoryEntryOverlayMarkup({
        entry,
        index,
        state,
        items,
        escapeHtml,
        inventoryEntryEquipmentSlot,
        inventoryEntryItemId,
        inventoryEntryDetailText,
        inventoryManualReorderEnabled,
        normalizeInventoryList,
        compareCandidateSummary,
        inventoryEntryLabel,
        inventoryEntryKindLabel,
      }),
    }),
    hostId,
    overlayId,
    activeModes,
    inventoryMode,
  });

  bindInteractionOverlay({
    state,
    documentObject,
    resolveEventChoice,
    resolveNpcService,
    closeInteraction,
    render,
  });

  return nextViewController;
}
